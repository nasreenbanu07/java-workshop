public class zone {
    private String name;
    private int zoneid;
    private String type;
    public void name(String name) {
          name = name;
       }

       public String getname() {
         return name;
       }   
    public void zoneid(int zoneid) {
          zoneid = zoneid;
       }

       public int getzoneid() {
         return zoneid;
       }  
    public void type(String type) {
          type = type;
       }

       public String gettype() {
         return type;
       }          

}
